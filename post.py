# Получение времени прихода на почту от пользователя (в формате 24 часа)
arrival_time = int(input("Введите время прихода на почту (часы): "))

# Проверка, что введенное время находится в допустимом диапазоне
if arrival_time < 8 or arrival_time >= 23:
    print("Почта закрыта. Приходите завтра с 8:00.")
else:
    # Проверка перерывов в работе
    if (10 <= arrival_time < 12) or (14 <= arrival_time < 15) or (18 <= arrival_time < 20):
        # Если гражданин пришел во время перерыва, он должен подождать до окончания перерыва
        next_time = 20 if 18 <= arrival_time < 20 else 15 if 14 <= arrival_time < 15 else 12
        print(f"Почта закрыта. Ожидайте до {next_time}:00.")
    else:
        # Гражданин может получить посылку через два часа
        delivery_time = arrival_time + 2
        if delivery_time >= 23:
            print("Почта закрывается. Приходите завтра с 8:00.")
        else:
            print(f"Вы сможете получить посылку в {delivery_time}:00.")
            